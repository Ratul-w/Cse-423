from OpenGL.GL import *
from OpenGL.GLUT import *
import random

class ball_design:
    def __init__(self, x, y, b_w_half, b_h_half):
        self.x = x
        self.y = y
        self.hx = random.choice([-1, 1])
        self.hy = random.choice([-1, 1])
        self.color = (random.random(), random.random(), random.random())
        self.w_half = b_w_half
        self.h_half = b_h_half


    def move(self, sp):

        self.x += self.hx * sp
        self.y += self.hy * sp
        if self.x >= self.w_half or self.x <= -self.w_half:
            self.hx *= -1
        if self.y >= self.h_half or self.y <= -self.h_half:
            self.hy *= -1



class make_box:
    def __init__(self):
        self.wid = 850
        self.hei = 850
        self.w_half = self.wid / 2
        self.h_half = self.hei / 2
        self.points_cord = []
        self.sp = 3.75
        self.shuttter = 0
        self.shuttter_state = 1
        self.froze_checker = 0
        self.f_cont = 0


        glutInit()
        glutInitDisplayMode(GLUT_DOUBLE | GLUT_RGB)
        glutInitWindowSize(self.wid, self.hei)
        glutCreateWindow(b"Amazing Box")

        glClear(GL_COLOR_BUFFER_BIT)
        glMatrixMode(GL_PROJECTION)
        glLoadIdentity()
        glOrtho(-self.w_half, self.h_half, -self.h_half, self.h_half, -1, 1)
        glMatrixMode(GL_MODELVIEW)

        glutDisplayFunc(self.output_back)
        glutIdleFunc(self.anima_part_view)
        glutKeyboardFunc(self.t2_key)
        glutSpecialFunc(self.t1_key)
        glutMouseFunc(self.m1)



    def to_coord(self, x, y):
        return x - (self.wid /2), (self.hei / 2) - y


    def draw_points(self):
        glPointSize(9)
        for a in self.points_cord:
            if self.shuttter and not self.shuttter_state:
                continue
            glColor3f(*a.color)
            glBegin(GL_POINTS)
            glVertex2f(a.x, a.y)
            glEnd()


    def draw_border(self):
        glColor3f(0.7, 0.7, 0.7)
        glBegin(GL_LINES)
        glVertex2f(-self.w_half, -self.h_half)
        glVertex2f(self.w_half, -self.h_half)
        glVertex2f(self.w_half, self.h_half)
        glVertex2f(-self.w_half, self.h_half)
        glEnd()


    def output_back(self):
        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT)
        glLoadIdentity()
        self.draw_border()
        self.draw_points()
        glutSwapBuffers()

    def anima_part_view(self):
        if not self.froze_checker:
            for x in self.points_cord:
                x.move(self.sp)
            if self.shuttter:
                self.f_cont += 1
                if self.f_cont % 50 == 0:
                    self.shuttter_state = 1 - self.shuttter_state
        glutPostRedisplay()


    def m1(self, button, state, a, b):
        if state != GLUT_DOWN:
            return
        if button == GLUT_RIGHT_BUTTON:
            hx, hy = self.to_coord(a, b)
            self.points_cord.append(ball_design(hx, hy, self.w_half, self.h_half))
        elif button == GLUT_LEFT_BUTTON:
            self.shuttter = 1 - self.shuttter

    def t1_key(self, key, a, b):
        if key == GLUT_KEY_UP:
            self.sp *= 1.2
        elif key == GLUT_KEY_DOWN:
            self.sp /= 1.2
            if self.sp < 0.2:
                self.sp = 0.2

    def t2_key(self, key, a, b):
        if key == b' ':
            self.froze_checker = 1 - self.froze_checker
    def run(self):
        glutMainLoop()

if __name__ == "__main__":
    make_box().run()